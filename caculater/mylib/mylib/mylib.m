#import "mylib.h"

@implementation mylib
-(int)add:(int)a addB:(int)b{
    return (a+b);
}

-(int)subtract:(int)a addB:(int)b{
    return (a-b);
}

-(int)multiply:(int)a addB:(int)b{
return (a*b);
}

-(double)divide:(double)a divideB:(double)b{
return (a/b);
}


@end


